export function useTranslation(isLangId: boolean) {
  const t = (key: string): string => {
    const translation = translations[key];
    if (!translation) {
      console.warn(`Translation missing for key: ${key}`);
      return key;
    }
    return isLangId ? translation.id : translation.en;
  };
  
  return { t };
}
